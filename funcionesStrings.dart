//Funciones para jugar con los Strings
void main() {
  String nombre = 'Enrique Reyes';
  print(nombre.toLowerCase());  //Convierte toda la cadena a minusculas
  print(nombre.toUpperCase()); //Convierte toda la cadena a mayusculas
  print(nombre.replaceAll(" ", "_")); //reemplaza el primer valor que se le pasa por el segundo en una cadena de texto 
}

